#include "b_libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int i;
	int res;

	res = 0;
	i = 0;
	while ((s1[i] != '\0' || s2[i] != '\0') && res == 0)
	{
		res = s1[i] - s2[i];
		i++;
	}
	return (res);
}
